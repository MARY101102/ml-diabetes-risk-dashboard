# ---------------------------------------------------------------------
# Service Class
# ---------------------------------------------------------------------

class DiabetesPredictionService:
    def __init__(self) -> None:
        self.model_package = self._load_model_package()
        self.base_model = self.model_package["base_model"]
        self.calibration_method = self.model_package.get(
            "calibration_method",
            "uncalibrated",
        )
        self.calibrator = self.model_package.get("calibrator")
        self.tree_model = self._extract_tree_model(self.base_model)
        self.explainer = shap.TreeExplainer(self.tree_model)

    def _load_model_package(self) -> dict[str, Any]:
        """
        Load calibrated model package.

        If calibrated model is not found, fallback to final uncalibrated model.
        """

        if CALIBRATED_MODEL_FILE.exists():
            return joblib.load(CALIBRATED_MODEL_FILE)

        if FINAL_MODEL_FILE.exists():
            model = joblib.load(FINAL_MODEL_FILE)

            return {
                "model_version": "diabetes_risk_model_v1_uncalibrated",
                "base_model": model,
                "calibration_method": "uncalibrated",
                "calibrator": None,
                "threshold": 0.5,
            }

        raise FileNotFoundError(
            "No diabetes model found. Run ML Step 5 and Step 6 first."
        )

    def _extract_tree_model(self, base_model: Any) -> Any:
        """
        Extract the Random Forest model from sklearn Pipeline.
        """

        if hasattr(base_model, "named_steps") and "model" in base_model.named_steps:
            return base_model.named_steps["model"]

        return base_model

    def _get_positive_class_shap_values(self, shap_values: Any) -> np.ndarray:
        """
        Handle different SHAP output formats.
        """

        if isinstance(shap_values, list):
            return shap_values[1]

        shap_array = np.asarray(shap_values)

        if shap_array.ndim == 3:
            return shap_array[:, :, 1]

        return shap_array

    def _create_feature_dataframe(self, request_data: dict[str, Any]) -> pd.DataFrame:
        """
        Convert API input into model-ready DataFrame in correct feature order.
        """

        row = {feature: request_data[feature] for feature in SELECTED_FEATURES_V1}
        return pd.DataFrame([row], columns=SELECTED_FEATURES_V1)

    def _apply_calibration(self, raw_probability: float) -> float:
        """
        Apply selected calibration method to raw model probability.
        """

        if self.calibration_method == "uncalibrated" or self.calibrator is None:
            return float(raw_probability)

        if self.calibration_method == "sigmoid":
            calibrated_probability = self.calibrator.predict_proba(
                np.array([[raw_probability]])
            )[:, 1][0]
            return float(calibrated_probability)

        if self.calibration_method == "isotonic":
            calibrated_probability = self.calibrator.predict(
                np.array([raw_probability])
            )[0]
            return float(calibrated_probability)

        return float(raw_probability)

    def _map_probability_to_risk(self, probability: float) -> str:
        """
        Convert calibrated probability into dashboard risk label.
        """

        if probability < 0.40:
            return "Low estimated risk"

        if probability < 0.70:
            return "Medium estimated risk"

        return "High estimated risk"

    def _create_explanation(self, X: pd.DataFrame) -> tuple[list[dict], list[dict]]:
        """
        Generate top increasing and reducing explanation factors.
        """

        shap_values = self.explainer.shap_values(X)
        positive_class_values = self._get_positive_class_shap_values(shap_values)

        row_shap_values = positive_class_values[0]

        explanation_df = pd.DataFrame(
            {
                "feature": SELECTED_FEATURES_V1,
                "label": [
                    FEATURE_LABELS.get(feature, feature)
                    for feature in SELECTED_FEATURES_V1
                ],
                "input_value": X.iloc[0].values.astype(float),
                "shap_value": row_shap_values.astype(float),
                "absolute_shap_value": np.abs(row_shap_values.astype(float)),
            }
        )

        explanation_df = explanation_df.sort_values(
            by="absolute_shap_value",
            ascending=False,
        )

        increasing_df = explanation_df[explanation_df["shap_value"] > 0].head(5)
        reducing_df = explanation_df[explanation_df["shap_value"] < 0].head(5)

        increasing_factors = [
            {
                "feature": row["feature"],
                "label": row["label"],
                "input_value": round(float(row["input_value"]), 4),
                "shap_value": round(float(row["shap_value"]), 6),
                "explanation": (
                    f"{row['label']} increased the model's estimated probability "
                    "for the positive dataset class."
                ),
            }
            for _, row in increasing_df.iterrows()
        ]

        reducing_factors = [
            {
                "feature": row["feature"],
                "label": row["label"],
                "input_value": round(float(row["input_value"]), 4),
                "shap_value": round(float(row["shap_value"]), 6),
                "explanation": (
                    f"{row['label']} reduced the model's estimated probability "
                    "for the positive dataset class."
                ),
            }
            for _, row in reducing_df.iterrows()
        ]

        return increasing_factors, reducing_factors

    def predict(self, request_data: dict[str, Any]) -> dict[str, Any]:
        """
        Run prediction and return dashboard-ready response.
        """

        X = self._create_feature_dataframe(request_data)

        raw_probability = float(self.base_model.predict_proba(X)[:, 1][0])
        calibrated_probability = self._apply_calibration(raw_probability)

        risk_label = self._map_probability_to_risk(calibrated_probability)

        increasing_factors, reducing_factors = self._create_explanation(X)

        return {
            "estimated_risk": risk_label,
            "calibrated_probability": round(calibrated_probability, 4),
            "display_probability": f"{calibrated_probability * 100:.1f}%",
            "raw_model_probability": round(raw_probability, 4),
            "calibration_method": self.calibration_method,
            "top_factors_increasing_estimated_risk": increasing_factors,
            "top_factors_reducing_estimated_risk": reducing_factors,
            "disclaimer": DISCLAIMER,
        }


diabetes_prediction_service = DiabetesPredictionService()